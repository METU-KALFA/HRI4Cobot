start = np.array([0., 0., 0.])
end = np.array([1., 1., 1.])

def processFeedback(feedback):
    s = "Feedback from marker '" + feedback.marker_name
    s += "' /control '" + feedback.control_name + "'"

    mp = ""
    if feedback.mouse_point_valid:
        mp = " at " + str(feedback.mouse_point.x)
        mp += ", " + str(feedback.mouse_point.y)
        mp += ", " + str(feedback.mouse_point.z)
        mp += " in frame " + feedback.header.frame_id

    if feedback.event_type == InteractiveMarkerFeedback.BUTTON_CLICK:
        rospy.loginfo( s + ": button click" + mp + "." )
    elif feedback.event_type == InteractiveMarkerFeedback.MENU_SELECT:
        rospy.loginfo( s + ": menu item " + str(feedback.menu_entry_id) + " clicked" + mp + "." )
    elif feedback.event_type == InteractiveMarkerFeedback.POSE_UPDATE:
        rospy.loginfo( s + ": pose changed")
    elif feedback.event_type == InteractiveMarkerFeedback.MOUSE_DOWN:
        rospy.loginfo( s + ": mouse down" + mp + "." )
    elif feedback.event_type == InteractiveMarkerFeedback.MOUSE_UP:
        rospy.loginfo( s + ": mouse up" + mp + "." )

    if feedback.marker_name == "start":
        global start
        start = np.array([feedback.pose.position.x, feedback.pose.position.y, feedback.pose.position.z])
    elif feedback.marker_name == "end":
        global end
        end = np.array([feedback.pose.position.x, feedback.pose.position.y, feedback.pose.position.z])

    waypoints = np.linspace(start, end, 10)
    waypoints = np.transpose(waypoints)
    drawer.visualize_path(waypoints)

    server.applyChanges()

def rand( min_, max_ ):
    return min_ + random()*(max_-min_)

def makeSphere( msg ):
    marker = Marker()

    marker.type = Marker.SPHERE
    marker.scale.x = msg.scale * 0.45
    marker.scale.y = msg.scale * 0.45
    marker.scale.z = msg.scale * 0.45
    marker.color.r = 0.5
    marker.color.g = 0.5
    marker.color.b = 0.5
    marker.color.a = 1.0

    return marker

def makeSphereControl(msg):
    control =  InteractiveMarkerControl()
    control.always_visible = True
    control.markers.append(makeSphere(msg))
    msg.controls.append(control)
    return control


